<script>
    $(document).ready( function() {

// calcul don cost
function calculCost(val){
	var nombre = $.isNumeric(val);
	if (nombre){

                    $("#montant-don").text(val);
            var numVal = Number(val);
            var reducAmont = numVal*66/100;
                reducAmont = numVal*66/100;
                numVal = numVal - reducAmont;
                $("#montant-reduit").text(numVal.toFixed(0));



	} else {
		$("#montant-don").text(0);
		$("#montant-reduit").text(0);
	};
};

 // bouton don occasionnel
 $(".mon-don").click(function(){
 	var valeur = $(this).attr("id");
 	$(".amount").attr("value",valeur);
 	$("#monthlyDon").val('S'); // reset monthly don
 	$(".montant").removeClass("selected");
 	$(this).addClass("selected");
 	calculCost(valeur);
 });
 // bouton don mensuel
 $(".monthly-don").click(function(){
 	var valeur = $(this).attr("id");
 	$(".amount").attr("value",valeur);
 	$("#monthlyDon").val('M'); // reset monthly don
 	$(".montant").removeClass("selected");
 	$(this).addClass("selected");
 	calculCost(valeur);
 });



 //saisie don occasionnel
$("#custom-single").keyup(function(){
 	this.value = this.value.replace(/[^0-9\.]/g,'');
 	$(".amount").val(this.value);
 	$("#monthlyDon").val('S'); // reset monthly don
 	calculCost(this.value);
 });
 $("#single").click(function(){
 	var valeur = $(this).find("input").val();
 	var nombre = $.isNumeric(valeur);
 	if (nombre){
 		$(".amount").val(valeur);
 	} else {
 		$(".amount").val(0);
 	};
 	$("#monthlyDon").val('S');
 	$(".montant").removeClass("selected");
 	$(this).addClass("selected");
 	calculCost(valeur);
 });

 // saisie don mensuel
 $("#custom-monthly").keyup(function(){
 	this.value = this.value.replace(/[^0-9\.]/g,'');
    $(".amount").val(this.value);
 	$("#monthlyDon").val('M');
 	calculCost(this.value);
 });
 $("#monthly").click(function(){
 	var valeur = $(this).find("input").val();
 	var nombre = $.isNumeric(valeur);
 	if (nombre){
 		$(".amount").val(valeur);
 	} else {
 		$(".amount").val(0);
 	};
 	$("#monthlyDon").val('M');
 	$(".montant").removeClass("selected");
 	$(this).addClass("selected");
 	calculCost(valeur);
 });

// show/hide champ organisation
$("#orga").click(function(){
	var isChecked = $('#orga').prop('checked');
	if(isChecked){
		$("#hidden-field").fadeIn();
	} else {
		$("#hidden-field").fadeOut();
	};
});

 // bouton don occasionnel
 $(".paiement-cb").click(function(){
 	$(".means").val('CB');
 });
 // bouton mensuel
 $(".paiement-pa").click(function(){
 	$(".means").val('PA');
 });

});
